-- Hangman Game
-- Hangman is a  paper and pencil guessing game for  two or more players.
-- One player  thinks of  a word and  the other(s) tries  to guess  it by
-- suggesting letters or numbers, within a certain number of guesses. For
-- each guess, we indicate which letters  in the secret word occur in the
-- guess, and the game ends when the guess is correct.
